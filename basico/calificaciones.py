# -*- coding: utf-8 -*-

calf = int(input("Ingresa tu calificación final "))
print ("Tu calificación es = ", calf)
if calf >=10 :
    print ("Excelente")
elif calf ==9 :
    print ("Muy Bien")
elif calf ==8 :
    print ("Bien")
elif calf ==7 :
    print ("Regular")
elif calf ==6 :
    print ("Suficiente")
else :
    print ("Reprobado")